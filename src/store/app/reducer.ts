const initialState: AppState = {
  ...imagesMapper(NonogramOptions[0].value),
  selectedOption: NonogramOptions[0],
  running: false,
  speed: SpeedOptions[2],
  clear: false,
};

const app = (state: AppState = initialState, action: Action) => {
  switch (action.type) {
    case APP_CONSTANTS.SET_RUNNING: {
      return {
        ...state,
        running: action.payload,
      };
    }
    case APP_CONSTANTS.CHANGE_OPTION: {
      const option: Option = action.payload;
      return {
        ...state,
        ...imagesMapper(option.value),
        selectedOption: option,
        running: false,
      }
    }
    case APP_CONSTANTS.CHANGE_SPEED: {
      const option: Option = action.payload;
      return {
        ...state,
        speed: option,
      };
    }
    case APP_CONSTANTS.CLEAR_FIELD: {
      return {
        ...state,
        clear: action.payload,
      };
    }
    case APP_CONSTANTS.SET_CUSTOM_FIELD: {
      return {
        ...state,
        rows: action.payload.rows,
        columns: action.payload.columns,
      };
    }
    default: {
      return state;
    }
  }
};

export default app;
